# append 

l1 = [11,25,200]
l1.append(0)
print(f'l1 = {l1}')
print()
print('------')
print()

# Append : adds an element at the end of the list.

# count

l2 = ['english','science','maths'] 
l2.count('english')
print(f'l2 = {l2}')
print()
print('------')
print()

# Count : returns the number of elements with the specified value.

# clear

l3 = ['ram','radha','reeta','geeta']
l3.clear()
print(f'l3 = {l3}')
print()
print('------')
print()

# Clear : removes all elements from the list

# copy

l4 = [11,22,45,67]
x = l4.copy()
print(x)
print()
print('-----')
print()

# Copy : returns the copy of the list.

# pop

l5 = ['c++','java','power apps']
l5.pop(1)
print(f'l5 = {l5}')
print()
print('------')
print()

# Pop : removes the element at the specified position.

# index 

l6 = ['abc','pqr','klm','xyz']
x = l6.index('klm')
print(x)
print()
print('------')
print()

# Index : returns the index of the first element with the specified value.

# extend

l7 = ['x','y','z']
l8 = [15,37,45]
l7.extend(l8)
print(l7)
print()
print('------')
print()

# Extend : adds the element of a list (or any iterable) to the end of the current list.

# insert

l9 = ['c#','.net','power automation']
l9.insert(0,'power bi')
print(f'l9 = {l9}')
print()
print('------')
print()

# Insert : adds an element at the specified position.

# sort

l10 = [11,35,9,40,100,105]
l10.sort()
print(l10)
print()
print('-----')
print()

# Sort : sorts the list

# remove

l11 = ['a','am','we']
l11.remove('we')
print(l11)
print()
print('-----')
print()

# Remove : removes the first item with the specified value.

# reverse

l12 = ['a','am','we']
l12.reverse()
print(l12)
print()
print('-----')
print()

# Reverse : reverses the order of the list.



